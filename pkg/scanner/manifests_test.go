// Each parser test uses a realistic snippet from the documented format
// and asserts that AI-relevant deps come back populated while irrelevant
// deps are silently dropped (we're an AI scanner, not a generic SBOM).

func TestParsePoetryLock_PicksUpAITorchEntries(t *testing.T) {
	path := createTempFile(t, "poetry.lock", `# This file is automatically generated, DO NOT EDIT.

[[package]]
name = "numpy"
version = "1.26.0"
description = "Fundamental package for array computing"

[[package]]
name = "torch"
version = "2.1.0"
description = "Tensors and Dynamic neural networks in Python"

[[package]]
name = "transformers"
version = "4.30.0"
description = "State-of-the-art Machine Learning"
`)
	deps := parsePoetryLock(path)
	if len(deps) != 2 {
		t.Fatalf("got %d deps, want 2 (torch + transformers; numpy not in catalog): %#v", len(deps), deps)
	}
	names := map[string]string{}
	for _, d := range deps {
		names[d.Name] = d.Version
	}
	if names["torch"] != "2.1.0" {
		t.Errorf("torch version = %q, want 2.1.0", names["torch"])
	}
	if names["transformers"] != "4.30.0" {
		t.Errorf("transformers version = %q, want 4.30.0", names["transformers"])
	}
}

func TestParsePipfileLock_PicksUpAIDeps(t *testing.T) {
	path := createTempFile(t, "Pipfile.lock", `{
  "_meta": {"hash": {"sha256": "..."}},
  "default": {
    "torch": {"version": "==2.1.0", "hashes": ["..."]},
    "requests": {"version": "==2.31.0"}
  },
  "develop": {
    "transformers": {"version": "==4.30.0"}
  }
}`)
	deps := parsePipfileLock(path)
	if len(deps) != 2 {
		t.Fatalf("got %d deps, want 2 (torch from default + transformers from develop): %#v", len(deps), deps)
	}
	names := map[string]string{}
	for _, d := range deps {
		names[d.Name] = d.Version
	}
	if names["torch"] != "2.1.0" {
		t.Errorf("torch version = %q (operator should be stripped)", names["torch"])
	}
}

func TestParsePnpmLock_PicksUpAIDeps(t *testing.T) {
	path := createTempFile(t, "pnpm-lock.yaml", `lockfileVersion: '6.0'
packages:
  /lodash@4.17.21:
    resolution: {integrity: sha512-x}
  /openai@4.52.5:
    resolution: {integrity: sha512-y}
  /@anthropic-ai/sdk@0.20.0:
    resolution: {integrity: sha512-z}
`)
	deps := parsePnpmLock(path)
	// targetAILibraries currently catalogs "openai" — @anthropic-ai/sdk
	// would only match if catalog has that scoped key. Spot-check
	// what we get back rather than over-asserting on catalog contents.
	gotOpenAI := false
	for _, d := range deps {
		if d.Name == "openai" && d.Version == "4.52.5" {
			gotOpenAI = true
		}
	}
	if !gotOpenAI {
		t.Errorf("expected openai@4.52.5 in pnpm output, got: %#v", deps)
	}
}

