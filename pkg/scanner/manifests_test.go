func TestParseCondaEnv_PicksUpAIDeps(t *testing.T) {
	path := createTempFile(t, "environment.yml", `name: my-ai-env
channels:
  - conda-forge
  - defaults
dependencies:
  - python=3.11
  - numpy=1.26.0
  - pip:
      - torch==2.1.0
      - transformers==4.30.0
`)
	deps := parseCondaEnv(path)
	names := map[string]string{}
	for _, d := range deps {
		names[d.Name] = d.Version
	}
	if names["torch"] != "2.1.0" {
		t.Errorf("expected torch=2.1.0 from pip sub-block, got: %#v", deps)
	}
	if names["transformers"] != "4.30.0" {
		t.Errorf("expected transformers=4.30.0 from pip sub-block, got: %#v", deps)
	}
}

func TestParseCondaEnv_StopsAtNextTopLevelKey(t *testing.T) {
	// If the parser failed to exit the dependencies: section, this
	// line ("variables:" with subsequent key=value pairs) might leak
	// into output. Confirms the section-toggle logic.
	path := createTempFile(t, "environment.yml", `dependencies:
  - torch==2.1.0
variables:
  HF_HOME: /opt/cache
`)
	deps := parseCondaEnv(path)
	if len(deps) != 1 || deps[0].Name != "torch" {
		t.Errorf("expected single torch entry, got: %#v", deps)
	}
}

// Integration: drop all five new manifest types into a temp project
// and run PerformScan end-to-end. Confirms the wiring in scanner.go's
// walk dispatches to each new parser.
func TestPerformScan_PicksUpNewManifestFormats(t *testing.T) {
	dir := createTempDir(t, map[string]string{
		"poetry.lock": `[[package]]
name = "torch"
version = "2.1.0"
`,
		"Pipfile.lock": `{
  "default": {"transformers": {"version": "==4.30.0"}},
  "develop": {}
}`,
		"pnpm-lock.yaml": `packages:
  /openai@4.52.5:
    resolution: {}
`,
		"yarn.lock": `"openai@^4.0.0":
  version "4.52.5"
`,
		"environment.yml": `dependencies:
  - pip:
      - langchain==0.1.0
`,
	})

	bom := PerformScan(dir)
	ecosystems := map[string]bool{}
	for _, d := range bom.Dependencies {
		ecosystems[d.Ecosystem] = true
	}
	for _, want := range []string{
		"Python (Poetry lock)",
		"Python (Pipfile lock)",
		"Node.js (pnpm lock)",
		"Node.js (yarn lock)",
		"Python (Conda environment.yml)",
	} {
		if !ecosystems[want] {
			t.Errorf("PerformScan missing ecosystem %q\nseen: %#v\npath: %s",
				want, ecosystems, filepath.Base(dir))
		}
	}
}
